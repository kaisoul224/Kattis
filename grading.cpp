#include <bits/stdc++.h>
using namespace std;

int main()
{
    int a,b,c,d,e,n;
    scanf("%d %d %d %d %d %d",&a,&b,&c,&d,&e,&n );
    if (n>=a) {printf("A\n"); return 0; }
    if (n>=b) {printf("B\n"); return 0; }
    if (n>=c) {printf("C\n"); return 0; }
    if (n>=d) {printf("D\n"); return 0; }
    if (n>=e) {printf("E\n"); return 0; }
    printf("F\n");
    return 0;
}
